	<h3>3. Postavljanje i prijava na termina</h3>
	
	<p>Da bi postavio svoj termin korisnik mora da bude registrovan. U određenoj sekciji (Fudbal ili Košarka) pomoću jednostavne forme unose informacije o terminu (mesto, vreme, cena i broj potrebnih igrača). Svi uneti podaci čuvaju se u bazi podataka dok god je ponuda aktivna, nakon čega se uklanjaju iz nje.</p>
	
	<p>Da bi se prijavio na postojeći termin, korisnik takođe mora da bude registrovan. Klikom na pristupno dugme odrđenog termina podnosi se zahtev za pristup, nakon čega autor posta može da prihvati ili odbije zahtev. Ako Vas nakon prijave postavljač termina prihvati, a Vi se bez prethodnog obaveštenja na istom ne pojavite, to povlači određene disciplinske mere</p>
	
	<h3>4. Ocena korisnika</h3>
	
	<p>Korisnik koji nudi termin i oni koji su se prijavili na isti mogu da ocene jedni druge, što utiče na reputaciju korisnika. Ovo se sprovodi radi što boljeg uvida u to da li je korisnik, bilo da nudi termin ili šalje zahtev za pristup, „osoba od poverenja“.</p>
	
	<h3>5. Postavljanje reklame</h3>
	
	<p>Ova funkcionalnost omogućena je samo VIP korisnicima. Oni u određenoj sekciji mogu da putem jednostavne forme unose informacije vezane za sportski objekat koji reklamiraju (naziv, lokacija, informacije, fotografije, slobodni termini...).</p>
	
	<h3>6. Nepoštovanje gorepomenutih pravila</h3>
	
	<p>Ukoliko korisnik očigledno krši navedena pravila ili ima jako nisku reputaciju, administracija sajta zadržava sva prava da istog banuje t.j. udalji sa sajta.</p>
	
	<h3>7. Primedbe i pitanja</h3>
	
	<p>Ukoliko imate bilo kakvu primedbu na rad sajta ili ponašanje nekog od njegovih korisnika ili bilo kakvo drugo pitanje ili sugestiju, možete se obratiti administraciji na mejl <b><i>kontakt@trebamiigrac.rs</b></i> i potrudićemo se da Vam odgovorimo u najkraćem mogućem roku.</p>
